SELECT SOL.ACIDLOTE                                                AS ID,
       SOL.CTIPOLOTE                                               AS TIPO,
       1                                                              "NVP-TIPOREG-G",
       EMP.CIRIF_CLIENTE || LPAD(EMP.NIT, 10, '0') || EMP.CATEGORY AS "NVP-IDSERVICIO-G",
       9999                                                        AS "NVP-SEC-BLQ-TAR-G",
       TO_CHAR(SYSDATE, 'YYYY-MM-DD')                              AS "NVP-FECHA-INF",
       SOL.NCANTREGS                                               AS "NVP-NUM-REGST",
       ' '                                                            "NVP-FILLER"
FROM MAESTRO_CLIENTES_TEBCA EMP
         JOIN MAESTRO_DEPOSITO CTA ON (CTA.ID_EXT_EMP = EMP.CIRIF_CLIENTE)
         JOIN TEB_LOTE SOL ON (SOL.ACCODCIA = EMP.COD_CLIENTE AND SOL.CTIPOLOTE IN ('1', '3') AND SOL.CESTATUS = '3')
WHERE EMP.CSTATUS = 'A'
  AND SOL.ACIDLOTE IN (SELECT IDLOTE AS B FROM NOVO_LOTE_EMI UNION ALL SELECT ID_LOTE AS B FROM TEB_LOTE_EMISION_TI)
ORDER BY EMP.COD_CLIENTE, SOL.ACIDLOTE;

SELECT LPAD(EMP.NIT, 10, '0') AS companyID,
       EMP.CIRIF_CLIENTE      AS accountNumber,
       EMP.ACOBSERVACION      AS companyType,
       SOL.ACIDLOTE           AS batchId,
       MAX(SEB.SECUENCIA) + 1 AS sequence
FROM TEB_LOTE SOL
         JOIN MAESTRO_CLIENTES_TEBCA EMP ON EMP.COD_CLIENTE = TO_NUMBER(SOL.ACCODCIA)
         JOIN SOLICITUD_EMISION_BNT SEB
              ON (SEB.ID_EMPRESA = LPAD(EMP.NIT, 10, '0') AND SEB.ID_CUENTA = EMP.CIRIF_CLIENTE)
WHERE SOL.ACIDLOTE = '5654'
GROUP BY EMP.NIT, EMP.CIRIF_CLIENTE, SOL.ACIDLOTE, EMP.ACOBSERVACION


